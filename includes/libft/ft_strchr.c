#include "libft.h"
#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (1)
	{
		if (*s == (char)c)
			return ((char *)s);
		else if (*s == '\0')
			return (NULL);
		s++;
	}
	return (NULL);
}
