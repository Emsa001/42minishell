#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	diff;

	while (1)
	{
		diff = (unsigned char) *s1 - (unsigned char) *s2;
		if (diff)
			return (diff);
		if (*s1 == '\0' || *s2 == '\0')
			break ;
		s1++;
		s2++;
	}
	return (0);
}
